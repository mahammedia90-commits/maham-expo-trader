function genUnits(prefix: string, count: number, basePrice: number, zones: string[]): ExpoUnit[] {
  const types = [
    { ar: "بوث قياسي", en: "Standard Booth", size: "3×3 م²", mult: 1 },
    { ar: "بوث مميز", en: "Premium Booth", size: "4×3 م²", mult: 1.5 },
    { ar: "بوث زاوية", en: "Corner Booth", size: "4×4 م²", mult: 1.8 },
    { ar: "جناح VIP", en: "VIP Pavilion", size: "6×4 م²", mult: 3 },
    { ar: "جزيرة عرض", en: "Island Display", size: "5×5 م²", mult: 2.5 },
    { ar: "كشك صغير", en: "Small Kiosk", size: "2×2 م²", mult: 0.6 },
    { ar: "منطقة F&B", en: "F&B Area", size: "4×3 م²", mult: 2 },
  ];
  const services = [
    { ar: "كهرباء", en: "Electricity" },
    { ar: "إنترنت فائق", en: "High-Speed Internet" },
    { ar: "تكييف مركزي", en: "Central AC" },
    { ar: "شاشة LED", en: "LED Screen" },
    { ar: "تنظيف يومي", en: "Daily Cleaning" },
    { ar: "أمن 24/7", en: "24/7 Security" },
    { ar: "مياه", en: "Water Supply" },
  ];
  const units: ExpoUnit[] = [];
  for (let i = 0; i < Math.min(count, 5); i++) {
    const t = types[i % types.length];
    const z = zones[i % zones.length];
    const price = Math.round(basePrice * t.mult / 100) * 100;
    const svcCount = Math.min(2 + Math.floor(t.mult), services.length);
    units.push({
      id: `${prefix}-U${String(i + 1).padStart(2, "0")}`,
      nameAr: `${t.ar} ${z}${String((i + 1) * 10 + 1).padStart(2, "0")}`,
      nameEn: `${t.en} ${z}${String((i + 1) * 10 + 1).padStart(2, "0")}`,
      zone: z,
      type: t.ar,
      typeEn: t.en,
      size: t.size,
      price,
      deposit: Math.round(price * 0.05 / 100) * 100,
      services: services.slice(0, svcCount).map(s => s.ar),
      servicesEn: services.slice(0, svcCount).map(s => s.en),
      available: true,
    });
  }
  return units;
}

